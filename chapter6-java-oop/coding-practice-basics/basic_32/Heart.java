package basic_32;

public class Heart {
    int heartRate;
    String bloodGroup;

    public void setHeartRate(int heartRate) {
        this.heartRate = heartRate;
    }

    public void setBloodGroup(String bloodGroup) {
        this.bloodGroup = bloodGroup;
    }

    public int getHeartRate() {
        return heartRate;
    }

    public String getBloodGroup() {
        return bloodGroup;
    }
}
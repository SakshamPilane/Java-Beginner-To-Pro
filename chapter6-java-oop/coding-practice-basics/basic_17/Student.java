package basic_17;

public class Student {
    private int rollNo;
    private String name;

    public int getRollNo()
    {
        return this.rollNo;
    }

    public void setRollNo(int rollNo)
    {
        this.rollNo = rollNo;
    }

    public String getName()
    {
        return this.name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}
